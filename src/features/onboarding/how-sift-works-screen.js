export default function HowSiftWorksScreen() {
  const insets = useSafeAreaInsets();
  const router = useRouter();

  // skip still exits onboarding; continue advances to the mandatory ack.
  const cont = () => {
    haptics.tap();
    router.push('/ack');
  };
  const skip = () => {
    haptics.tap();
    router.replace('/today');
  };

  return (
    <View style={[styles.root, { paddingTop: insets.top, paddingBottom: insets.bottom }]}>
      <OnboardingTopBar onSkip={skip} />

      <ScrollView
        style={styles.scroll}
        contentContainerStyle={styles.scrollContent}
        showsVerticalScrollIndicator={false}
      >
        <Text style={styles.heading}>How Sift works</Text>
        <Text style={styles.lede}>
          Four things worth knowing before you start.
        </Text>

        <View style={styles.bullets}>
          {BULLETS.map((b, i) => (
            <Bullet key={i} {...b} />
          ))}
        </View>
      </ScrollView>

      <View style={styles.bottom}>
        <Button onPress={cont} fullWidth accessibilityLabel="Continue">
          Continue
        </Button>
      </View>
    </View>
  );
}

